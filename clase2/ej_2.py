"""
Implementar un programa en Python que pida al usuario que ingrese dos números y muestre la suma, resta, división y multiplicación de ambos.
"""
entrada1 = float(input("Ingresar numero uno:"))
entrada2 = float(input("Ingresar numero dos:"))

print(f"suma: {entrada1 + entrada2}")
print(f"resta: {entrada1 - entrada2}")
print(f"division: {entrada1 / entrada2}")
print(f"multiplicacion: {entrada1 * entrada2}")
